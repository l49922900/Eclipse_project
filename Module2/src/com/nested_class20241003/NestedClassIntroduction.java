package com.nested_class20241003;

public class NestedClassIntroduction {

	/*
	嵌套類別（Nested Class）:是指在另一個類別內部定義的類別
	 
	***現在基本上很少用到了
	
	分為以下幾種類型:
	1.靜態嵌套類別（Static Nested Class）
	2.內部類別（Inner Class）
		(1)成員內部類別（Member Inner Class）
		(2)匿名內部類別（Anonymous Inner Class）
		(3)局部內部類別（Local Inner Class）
	
	
	
	 為什麼使用嵌套類別？

			1.邏輯分組：當一個類別僅在另一個類別內部使用時，將其作為嵌套類別有助於邏輯上的組織。
			2.增加封裝性：嵌套類別可以訪問外部類別的私有成員，從而增強封裝性。
			3.提高可讀性和維護性：相關的類別放在一起，使代碼更易於理解和維護。
			4.減少命名空間污染：嵌套類別不會在外部類別的包中佔用額外的命名空間。
	 */
 
 

}
