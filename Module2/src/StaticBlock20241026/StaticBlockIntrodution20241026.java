package StaticBlock20241026;

public class StaticBlockIntrodution20241026 {
/*
靜態區塊（Static Block）:
	是一段會在類別（class）被載入時執行的程式碼。靜態區塊通常用來執行類別層級的初始化操作
	

靜態區塊的特性

	1.在類別載入時執行：靜態區塊會在 JVM（Java Virtual Machine）載入類別時自動執行，且只執行一次。
	2.優先於建構子執行：靜態區塊會在任何建構子（Constructor）執行之前執行。
	3.初始化靜態變數：通常用來初始化靜態變數（Static Variable），例如從檔案中載入設定或進行其他只需執行一次的初始化操作。


靜態區塊的用途

	1.設定靜態變數的初始值，例如載入系統配置檔案或資料庫連線資訊。
	2.執行一些只需執行一次的程式邏輯，例如註冊程式所需的驅動。
	3.在專案開始前進行一些資源的初始化或檢查。
	
注意事項

	1.多個靜態區塊會按順序執行，因此在程式中可以使用多個靜態區塊，執行順序以程式碼出現的先後為準。
 */
	
	
	//語法:
	static {
        // 初始化操作
        System.out.println("靜態區塊執行中...");
    }
	
}
